extension MutableCollection where Index == Int {
    
    internal mutating func shuffling() {
        guard count > 1 else { return }
        
        for i in 0..<count - 1 {
            let j = Int(arc4random_uniform(UInt32(count - i))) + i
            guard i != j else { continue }
            self.swapAt(i, j)
        }
    }
}



